// concat
const array11 = [1, 2, 3];
const array12 = [4, 5, 6];
const concated = array11.concat(array12);
console.log(concated);

// reverse
const array2 = [1, 2, 3];
array2.reverse();
console.log(array2);

// push
const array3 = [1, 2, 3];
array3.push(4, 5, 6);
console.log(array3);

// unshift
const array4 = [1, 2, 3];
array4.unshift(4, 5, 6);
console.log(array4);

